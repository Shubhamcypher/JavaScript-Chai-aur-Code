const month = "march"

switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        
    case "april":
        console.log("april");
        

    default:
        console.log("default case match");
        break;
}
//not writing break will switch to every bottom case even to the default